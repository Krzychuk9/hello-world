package pl.kasprowski.helloworldakademiakodu;

import java.util.*;

public class zad7 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a,b;
		System.out.println("Podaj a: ");
		a = sc.nextInt();
		System.out.println("Podaj b: ");
		b = sc.nextInt();
		
		System.out.println("a + b = " + (a + b) );
		System.out.println("a - b = " + (a - b) );
		System.out.println("a * b = " + (a * b) );
		System.out.println("a / b = " + (a / b) );
		System.out.println("a % b = " + (a % b) );
	}

}
